



'''
p(A) = 0.8 - купим изделие 1 типа, !p(A) = 0.2 - не купим изделие 1 типа
p(B) = 0.9 - купим изделие 2 типа, !p(B) = 0.1 - не купим изделие 2 типа
p(C) = 0.85 - купим изделие 3 типа, !p(C) = 0.15 - не купим изделие 3 типа


[!p(A) * p(B) * p(C)] + [p(A) * !p(B) * p(C)] + [p(A) * p(B) * !p(C)] - то что мы ищем
'''
A = 0.8; nA = 0.2
B = 0.9; nB = 0.1
C = 0.85; nC = 0.15


print(
    (nA * B * C) + (A * nB * C) + (A * B * nC)
)