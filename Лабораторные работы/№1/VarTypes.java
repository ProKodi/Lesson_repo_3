



class VarTypes{
    public static void main(String args[]){
        int varPrimitive;
        varPrimitive = 1;
        int varReferense[];
        varReferense = new int[3];
        varReferense[0] = 2;
        varReferense[1] = 3;
        varReferense[2] = 4;
        System.out.println("varPrimitive = " + varPrimitive); 
        System.out.println("varReferense[0] = " + varReferense[0]);
        System.out.println("varReferense[1] = " + varReferense[1]);
        System.out.println("varReferense[2] = " + varReferense[2]);
    }
}

